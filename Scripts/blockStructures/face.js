class face{
    //holds data for an individual block face (rotation, color, etc)
    constructor(x, y, z, rx, ry, rz, color, width, height){
        this.color = color;
        this.x = x;
        this.y = y;
        this.z = z;
        this.rx = rx;
        this.ry = ry;
        this.rz = rz;

        this.width = width;
        this.height = height;
    }
}